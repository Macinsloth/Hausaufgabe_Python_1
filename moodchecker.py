mood = input("How do you feel?:")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times!")
elif mood == "sad":
    print("Google puppies!")
elif mood == "excited":
    print("That is great!")
elif mood == "relaxed":
    print("Enjoy it!")
else:
    print("Cheer up, mate!")
